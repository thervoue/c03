#include <unistd.h>

void	ft_print_comb(void)
{
	char c;
	char d;
	char u;

	u = '2' - 1;
	d = '1' - 1;
	c = '0' - 1;
	while (c++ < '7')
	{
		d = c;
		while (d++ < '8')
		{
			u = d;
			while (u++ < '9')
			{
				write(1, &c, 1);
				write(1, &d, 1);
				write(1, &u, 1);
				if (c < '7')
				{
					write(1, ", ", 2);
				}
			}
		}
	}
}
